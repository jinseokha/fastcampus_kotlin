// 06. 연산자

// 산술 연산자
// +, -, *, /(몫), %(나머지)

// 대입 연산자
// 좌변 = 우변 (우변 값이 좌변에 들어감)
// a = 5 (o), 5 = a (x)

// 복합 대입 연산자
// +=, -=, *=, /=, %=
// a += 10 -> a = a + 10

// 증감 연산자
// ++, --
// a++ , ++a

// 비교 연산자
// >, >=, <, <=, ==, !=
// true == true -> true
// true == false -> false
// true != true -> false
// true != false -> true

// 논리 연산자
// &&, ||, !
// true && true -> true
// true | false -> true
// !true -> false
// !false -> true






